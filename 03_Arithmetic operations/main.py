num1 = 10
num2 = 5

# Addition
result_add = num1 + num2
print(f"Addition >>  {num1} + {num2} = {result_add}")

# Subtraction
result_sub = num1 - num2
print(f"Subtraction >>  {num1} - {num2} = {result_sub}")

# Multiplication
result_mul = num1 * num2
print(f"Multiplication >>  {num1} * {num2} = {result_mul}")

# it will return division result as float numbers
# Division
result_div = num1 / num2   
print(f"Division >>  {num1} / {num2} = {result_div}")

#  it would return whole number after rounding numbers
# Floor Division
result_floor_div = num1 // num2 
print(f"Floor Division >>  {num1} // {num2} = {result_floor_div}") 

#  rmeinder
result_rem = num1 % num2
print(f"Remainder >>  {num1} % {num2} = {result_rem}")

# Exponentiation
result_exp = num1 ** num2  
print(f"Exponentiation >>  {num1} ** {num2} = {result_exp}")


# absolute value
num3 = -15
print(f"Absolute Value >>  |{num3}| = {abs(num3)}")
